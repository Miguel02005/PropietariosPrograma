    private void agregarPropietario() throws NumberFormatException {
        String nombre = jTextField1.getText();
        String apellido = jTextField2.getText();
        int edad = Integer.parseInt(jTextField3.getText());
        long documento = Long.parseLong(jTextField4.getText());
        String id = jTextField5.getText();

        propietario nuevoPropietario = new propietario(nombre, apellido, edad, (int) documento, id);
        propietariosList.add(nuevoPropietario);

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");

        System.out.println("Propietario agregado: " + nuevoPropietario.getNombre());
    }
    
